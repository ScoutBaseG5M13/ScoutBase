package es.dimecresalessis.scoutbase.domain.user.model;

/**
 * Enumeration for User Permissions.
 * <p>
 * This enum lists specific permissions that define user access rights within the application.
 * Permissions can be associated with roles for access control.
 * </p>
 */
public enum Permission {
    PLAYER_WRITE,
    PLAYER_READ,
    USER_WRITE,
    USER_READ
}
